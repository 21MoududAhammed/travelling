document.getElementById("moudud").addEventListener("click", () => {
  window.location.hash = "devis1";
  const element = document.getElementById("devis1");
  const content = document.getElementById("moudud-heading");
  console.log(content.innerText);
  element.style.backgroundColor = "#E8F9F3"; // Change the background color to green
  element.style.borderLeft = "1px solid green"; // Add a red border on the left side
  element.style.transition = "1000";
  setTimeout(() => {
    element.style.backgroundColor = ""; // Remove the background color after 2 seconds
    element.style.borderLeft = ""; // Remove the left border after 2 seconds
  }, 2000);
});



